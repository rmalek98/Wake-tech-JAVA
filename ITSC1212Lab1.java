//Write a Java class with following criteria - The class name is MyClass. It has no fields and a main method. The main method should print the odd numbers from 11 to 50

 class MyClass {  
//Write a Java class with following criteria - The class name is MyClass. It has no fields and a main method. The main method should print the odd numbers from 11 to 50

    public static void main(String[] args) {
        for (int i = 11; i <= 50; i++) {
            if (i % 2 != 0) {
                System.out.println(i);
            }
        }
    } 
    


 }